# MARK IN-PROGRESS
# ═══════════════════════════════════════════════════════════════════════════
@login_required
def service_start(request, pk):
    svc = get_object_or_404(CustomerService, pk=pk)
    if request.method == 'POST':
        if svc.status == ServiceStatus.DRAFT:
            svc.status = ServiceStatus.IN_PROGRESS
            svc.save(update_fields=['status', 'updated_at'])
            messages.success(request, f'Service {svc.service_number} marked as In Progress.')
        else:
            messages.error(request, 'Only DRAFT services can be started.')
    return redirect('service_detail', pk=pk)


# ═══════════════════════════════════════════════════════════════════════════
# COMPLETE — deducts inventory + generates invoice with COGS/ROI
# ═══════════════════════════════════════════════════════════════════════════
@login_required
@transaction.atomic
def service_complete(request, pk):
    svc = get_object_or_404(CustomerService, pk=pk)
    if request.method != 'POST':
        return redirect('service_detail', pk=pk)

    if svc.status not in (ServiceStatus.DRAFT, ServiceStatus.IN_PROGRESS):
        messages.error(request, 'Only Draft or In Progress services can be completed.')
        return redirect('service_detail', pk=pk)

    now = timezone.now()
    lines = list(svc.lines.select_related(
        'item__default_unit', 'item__selling_unit',
        'unit', 'location__warehouse',
    ).all())
    other_mats = list(svc.other_materials.all())

    # ── Inventory deduction (Product Lines only) ───────────────────────────
    if lines:
        try:
            from inventory.models import StockMove, StockBalance, MoveType, MoveStatus
            from catalog.models import convert_to_base_unit
            from warehouses.models import Location

            default_location = None
            if svc.warehouse_id:
                default_location = (
                    Location.objects
                    .filter(warehouse=svc.warehouse, is_pickable=True)
                    .order_by('code')
                    .first()
                )

            missing_location_items = []
            moves = []
            for line in lines:
                location = line.location or default_location
                if location is None:
                    missing_location_items.append(line.item.code)
                    continue

                base_qty = convert_to_base_unit(
                    line.qty, line.unit, line.item.stock_unit, item=line.item
                )
                move = StockMove(
                    move_type=MoveType.SERVICE_OUT,
                    item=line.item,
                    qty=base_qty,
                    unit=line.item.stock_unit,
                    from_location=location,
                    to_location=None,
                    reference_type='CustomerService',
                    reference_id=svc.pk,
                    reference_number=svc.service_number,
                    batch_number='',
                    serial_number='',
                    status=MoveStatus.POSTED,
                    created_by=request.user,
                    posted_by=request.user,
                    posted_at=now,
                )
                moves.append(move)

                balance, _ = StockBalance.objects.select_for_update().get_or_create(
                    item=line.item,
                    location=location,
                    defaults={'qty_on_hand': Decimal('0'), 'qty_reserved': Decimal('0')},
                )
                balance.qty_on_hand -= base_qty
                wh = location.warehouse
                if not wh.allow_negative_stock and balance.qty_on_hand < 0:
                    raise ValueError(
                        f"Insufficient stock for {line.item.code} at {location}. "
                        f"Available: {balance.qty_on_hand + base_qty}, Requested: {base_qty}"
                    )
                balance.save()

            if moves:
                StockMove.objects.bulk_create(moves)

            if missing_location_items:
                messages.warning(
                    request,
                    f'No location set for item(s): {", ".join(missing_location_items)}. '
                    'Their stock was NOT deducted. Set a location or assign a warehouse.'
                )

        except ValueError as exc:
            messages.error(request, f'Stock error: {exc}')
            return redirect('service_detail', pk=pk)

    # ── Compute COGS for invoice ───────────────────────────────────────────
    try:
        from catalog.utils import get_item_cogs_for_unit
        _cogs_fn = get_item_cogs_for_unit
    except ImportError:
        _cogs_fn = None

    total_cogs = Decimal('0')
    for line in lines:
        if _cogs_fn:
            try:
                cogs_pu = _cogs_fn(line.item, line.unit)
            except Exception:
                cogs_pu = line.item.cost_price or Decimal('0')
        else:
            cogs_pu = line.item.cost_price or Decimal('0')
        total_cogs += cogs_pu * line.qty

    # Other materials: their unit_price is treated as cost (no catalog reference)
    for mat in other_mats:
        total_cogs += mat.line_total

    # ── Generate Invoice ───────────────────────────────────────────────────
    from core.models import Invoice, InvoiceLine
    from core.views import _next_invoice_number

    subtotal = svc.subtotal
    discount_amt = svc.discount_amount
    grand_total = svc.grand_total

    inv = Invoice.objects.create(
        invoice_number=_next_invoice_number(),
        date=now.date(),
        customer_name=svc.customer_name,
        customer_address=svc.address,
        subtotal=subtotal,
        discount_total=discount_amt,
        grand_total=grand_total,
        grand_total_cogs=total_cogs,
        notes=f'Service: {svc.service_name}',
        created_by=request.user,
    )

    # Product line items
    for line in lines:
        InvoiceLine.objects.create(
            invoice=inv,
            item_code=line.item.code,
            item_name=line.item.name,
            qty=line.qty,
            unit=line.unit.abbreviation,
            unit_price=line.unit_price,
            line_total=line.line_total,
        )

    # Other material items
    for mat in other_mats:
        InvoiceLine.objects.create(
            invoice=inv,
            item_code='MAT',
            item_name=mat.item_name,
            qty=mat.qty,
            unit='unit',
            unit_price=mat.unit_price,
            line_total=mat.line_total,
        )

    # Service fee line (if set)
    fee = svc.service_fee_amount
    if fee > 0:
        InvoiceLine.objects.create(
            invoice=inv,
            item_code='SVC-FEE',
            item_name='Service / Labor Fee',
            qty=Decimal('1'),
            unit='svc',
            unit_price=fee,
            line_total=fee,
        )

    # If nothing at all, create summary line
    if not lines and not other_mats and fee == 0:
        InvoiceLine.objects.create(
            invoice=inv,
            item_code='SVC',
            item_name=svc.service_name,
            qty=Decimal('1'),
            unit='svc',
            unit_price=grand_total,
            line_total=grand_total,
        )

    # ── Mark service completed ─────────────────────────────────────────────
    svc.status = ServiceStatus.COMPLETED
    svc.invoice = inv
    svc.posted_by = request.user
    svc.posted_at = now
    if not svc.completion_date:
        svc.completion_date = now.date()
    svc.save(update_fields=[
        'status', 'invoice', 'posted_by', 'posted_at',
        'completion_date', 'updated_at',
    ])

    messages.success(
        request,
        f'Service {svc.service_number} completed. Invoice {inv.invoice_number} generated.'
    )
    return redirect('service_detail', pk=pk)


